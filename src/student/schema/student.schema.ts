//###########################################################################################
import * as mongoose from 'mongoose';
//###########################################################################################

//###########################################################################################
export const StudentSchema = new mongoose.Schema(
  {
    name: { type: String, required: true },
    email: { type: String, required: true },
  },
  { timestamps: true },
);
//###########################################################################################

//###########################################################################################
StudentSchema.index({ email: 1 }, { unique: true });
//###########################################################################################
